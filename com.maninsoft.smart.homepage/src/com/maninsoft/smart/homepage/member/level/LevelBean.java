package com.maninsoft.smart.homepage.member.level;

public class LevelBean {

    private int levIdx;
    private String levId;
    private String levName;
    private String levStatus;

    public LevelBean() {
    }

    public int getLevIdx() {
        return levIdx;
    }

    public void setLevIdx(int levIdx) {
        this.levIdx = levIdx;
    }

    public String getLevId() {
        return levId;
    }

    public void setLevId(String levId) {
        this.levId = levId;
    }

    public String getLevName() {
        return levName;
    }

    public void setLevName(String levName) {
        this.levName = levName;
    }

    public String getLevStatus() {
        return levStatus;
    }

    public void setLevStatus(String levStatus) {
        this.levStatus = levStatus;
    }

}